"""
LLM Judge for evaluating trading strategy code alignment with task requirements.

Supports 2 connection modes:
1. API mode (default) - any OpenAI-compatible endpoint (OpenRouter, OpenAI, etc.)
2. SGLang mode - local SGLang server
"""

import logging
import os
import re
from typing import Dict, Optional, Tuple

from openai import OpenAI

logger = logging.getLogger(__name__)


class StrategyJudge:
    """
    LLM-based judge for evaluating whether generated strategy code matches task requirements.

    The judge checks:
    - Whether the strategy uses indicators mentioned in the task
    - Whether the strategy solves the requested problem
    - Whether the code is relevant to the task description
    """

    def __init__(
        self,
        mode: str = "api",
        api_key: Optional[str] = None,
        base_url: Optional[str] = None,
        model: Optional[str] = None,
        enable_thinking: bool = False,
    ):
        """
        Initialize the strategy judge.

        Args:
            mode: Connection mode - "api" (default) or "sglang"
            api_key: API key (optional, depends on mode)
            base_url: Base URL for API (optional, depends on mode)
            model: Model name (required via parameter or JUDGE_MODEL env var)
            enable_thinking: Whether to enable thinking mode for the judge model
        """
        self.mode = mode
        self.enable_thinking = enable_thinking

        if mode == "api":
            self.client = OpenAI(
                api_key=api_key or os.environ.get("JUDGE_API_KEY", ""),
                base_url=base_url or os.environ.get("JUDGE_BASE_URL", ""),
            )
            self.model_name = model or os.environ.get("JUDGE_MODEL", "")

        elif mode == "sglang":
            self.client = OpenAI(
                base_url=base_url or "http://localhost:30000/v1",
                api_key="EMPTY",
            )
            self.model_name = model or os.environ.get("JUDGE_MODEL", "")

        else:
            raise ValueError(f"Unknown mode: {mode}. Use 'api' or 'sglang'")

        logger.info(f"StrategyJudge initialized with mode={mode}, model={self.model_name}")

    def evaluate(
        self,
        task_description: str,
        generated_code: str,
        timeout: float = 30.0,
    ) -> Tuple[bool, str, Dict]:
        """
        Evaluates whether the generated strategy code aligns with the task requirements.

        Args:
            task_description: The original task/prompt describing what strategy to build
            generated_code: The code generated by the model
            timeout: Timeout for API call in seconds

        Returns:
            Tuple of (is_aligned, explanation, metadata)
            - is_aligned: True if strategy matches task requirements, False otherwise
            - explanation: Text explanation from the judge
            - metadata: Additional metrics (score, reasoning, etc.)
        """
        try:
            evaluation_prompt = self._create_evaluation_prompt(task_description, generated_code)

            logger.debug(f"Calling judge API (mode={self.mode}, model={self.model_name})")

            request_params = {
                "model": self.model_name,
                "messages": [{"role": "user", "content": evaluation_prompt}],
                "max_tokens": 8000,
                "temperature": 0.0,
            }

            if self.mode == "sglang":
                request_params["extra_body"] = {
                    "chat_template_kwargs": {"enable_thinking": self.enable_thinking}
                }
            elif self.mode == "api" and not self.enable_thinking:
                request_params["extra_body"] = {
                    "chat_template_kwargs": {"enable_thinking": False}
                }

            response = self.client.chat.completions.create(**request_params)

            evaluation_text = response.choices[0].message.content.strip()
            is_aligned, score = self._parse_alignment(evaluation_text)

            logger.debug(f"Judge evaluation: is_aligned={is_aligned}, score={score}")

            metadata = {
                "judge_score": score,
                "judge_explanation": evaluation_text,
                "judge_mode": self.mode,
                "judge_model": self.model_name,
            }

            return is_aligned, evaluation_text, metadata

        except Exception as e:
            logger.error(f"Judge evaluation error: {e}")

            is_aligned, explanation = self._fallback_evaluation(task_description, generated_code)

            metadata = {
                "judge_score": 1 if is_aligned else 0,
                "judge_explanation": f"Fallback evaluation used due to error: {str(e)}",
                "judge_mode": "fallback",
                "judge_error": str(e),
            }

            return is_aligned, explanation, metadata

    def evaluate_strategy_alignment(
        self,
        task_description: str,
        generated_code: str,
        timeout: float = 30.0,
    ) -> Tuple[bool, str, Dict]:
        """Alias for evaluate() for backward compatibility."""
        return self.evaluate(task_description, generated_code, timeout)

    @staticmethod
    def _create_evaluation_prompt(task_description: str, generated_code: str) -> str:
        return f"""You are an expert in evaluating trading strategies. Your task is to determine whether the generated strategy code meets the requirements of the assignment.

**STRATEGY ASSIGNMENT:**
{task_description}

**GENERATED CODE:**
```python
{generated_code}
```

**EVALUATION CRITERIA:**

1. **Indicator compliance**: Does the code use the indicators or equivalent logic from the assignment? IMPORTANT: Accept reasonable simplifications if the assignment requires complex statistical methods (e.g. Johansen test) that are impossible in Backtrader. Simplifying to SMA/StdDev for Z-score is ACCEPTABLE.

2. **Problem solving**: Does the code implement the MAIN IDEA of the task? If the task is about mean reversion and the code uses Z-score and trades on deviations — this is COMPLIANT, even if details are simplified.

3. **Relevance**: Is the code related to the task description? The code must explicitly ignore ALL conditions of the assignment to receive 0.

**INSTRUCTIONS:**

Analyze the code on each criterion (1-2 sentences per criterion).

Then provide a final rating in the format "Rating: [[X]]", where:
- X = 1 if the code implements the MAIN LOGIC of the assignment (even with simplifications)
- X = 0 if the code COMPLETELY DOES NOT COMPLY (ignores the main idea)

IMPORTANT: Be LENIENT with simplifications. Penalize ONLY if the code clearly ignores the main task or uses completely different indicators/logic WITHOUT reason. If the assignment is technically impossible to implement exactly in Backtrader — accept reasonable simplifications.

You MUST end your response with the line "Rating: [[X]]"."""

    def _parse_alignment(self, evaluation_text: str) -> Tuple[bool, int]:
        """
        Parses the judge's evaluation text to extract alignment decision.

        Args:
            evaluation_text: The judge's response text

        Returns:
            Tuple of (is_aligned, score)
        """
        try:
            rating_patterns = [
                r'rating:\s*\[\[(\d+)\]\]',
                r'rating\s*\[\[(\d+)\]\]',
                r'\[\[(\d+)\]\]',
                r'rating:\s*(\d+)',
            ]

            evaluation_lower = evaluation_text.lower().strip()

            for pattern in rating_patterns:
                matches = re.findall(pattern, evaluation_lower)
                if matches:
                    try:
                        score = int(matches[-1])
                        is_aligned = (score == 1)
                        logger.debug(f"Found rating: {score} from text: {evaluation_text[:100]}...")
                        return is_aligned, score
                    except ValueError:
                        continue

            logger.warning("Rating pattern not found, using keyword fallback")

            positive_keywords = ["matches", "correct", "aligned", "yes", "compliant"]
            negative_keywords = ["doesn't match", "incorrect", "not aligned", "no", "non-compliant"]

            last_part = evaluation_lower[-300:]

            positive_count = sum(1 for keyword in positive_keywords if keyword in last_part)
            negative_count = sum(1 for keyword in negative_keywords if keyword in last_part)

            if positive_count > negative_count:
                logger.debug(f"Fallback: more positive words ({positive_count} vs {negative_count})")
                return True, 1
            elif negative_count > positive_count:
                logger.debug(f"Fallback: more negative words ({negative_count} vs {positive_count})")
                return False, 0

            logger.warning(f"Cannot determine alignment from: {evaluation_text[:500]}...")
            return False, 0

        except Exception as e:
            logger.error(f"Error parsing alignment: {e}")
            return False, 0

    def _fallback_evaluation(self, task_description: str, generated_code: str) -> Tuple[bool, str]:
        """
        Simple heuristic evaluation when judge API is unavailable.

        Checks for basic alignment:
        - Code is not empty
        - Code contains strategy class
        - Some common indicators are mentioned
        """
        if not generated_code or len(generated_code.strip()) < 100:
            return False, "Code is too short or empty"

        code_lower = generated_code.lower()
        task_lower = task_description.lower()

        indicator_keywords = [
            "sma", "ema", "rsi", "macd", "bollinger", "vix", "moving average",
        ]

        task_indicators = [kw for kw in indicator_keywords if kw in task_lower]

        if not task_indicators:
            has_strategy = "class" in code_lower and "bt.strategy" in code_lower
            return has_strategy, "No specific indicators in task, checking basic structure"

        code_has_indicators = any(ind in code_lower for ind in task_indicators)

        if code_has_indicators:
            return True, f"Code appears to use task indicators: {task_indicators}"
        else:
            return False, f"Code does not use task indicators: {task_indicators}"


def create_strategy_judge(
    mode: Optional[str] = None,
    api_key: Optional[str] = None,
    base_url: Optional[str] = None,
    model: Optional[str] = None,
    enable_thinking: bool = False,
) -> StrategyJudge:
    """
    Factory function to create a StrategyJudge with configuration from environment or parameters.

    Environment variables (optional):
    - JUDGE_MODE: "api" (default) or "sglang"
    - JUDGE_API_KEY: API key for the judge service
    - JUDGE_BASE_URL: Base URL for the judge service
    - JUDGE_MODEL: Model name
    - JUDGE_ENABLE_THINKING: "true" or "false"

    Args:
        mode: Override mode from environment
        api_key: Override API key from environment
        base_url: Override base URL from environment
        model: Override model from environment
        enable_thinking: Override thinking setting from environment

    Returns:
        StrategyJudge instance
    """
    mode = mode or os.environ.get("JUDGE_MODE", "api")
    api_key = api_key or os.environ.get("JUDGE_API_KEY")
    base_url = base_url or os.environ.get("JUDGE_BASE_URL")
    model = model or os.environ.get("JUDGE_MODEL")

    env_thinking = os.environ.get("JUDGE_ENABLE_THINKING", "").lower()
    if env_thinking in ["true", "1", "yes"]:
        enable_thinking = True
    elif env_thinking in ["false", "0", "no"]:
        enable_thinking = False

    return StrategyJudge(
        mode=mode,
        api_key=api_key,
        base_url=base_url,
        model=model,
        enable_thinking=enable_thinking,
    )


